# Custom python functions


def double_number(a):
    print(f"value before double_number(): {a}")
    result = a + a
    print(f"value after double_number(): {result}")
    return 0


def square_number(a):
    print(f"value before square_number(): {a}")
    result = a * a
    print(f"value after square_number(): {result}")
    return 0
